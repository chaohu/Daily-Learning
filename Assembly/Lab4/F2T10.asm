NAME	F2T10
EXTRN	RADIX:NEAR
PUBLIC	F2T10

.386
DATA	SEGMENT	USE16	PUBLIC
BUF	DB	12 DUP(?)
DATA	ENDS
CODE	SEGMENT	USE16	PUBLIC
	ASSUME	CS:CODE, DS:DATA
F2T10	PROC		
	PUSH	EBX
	PUSH	SI
	LEA	SI, BUF
	CMP	DX, 32
	JE	B
	MOVSX	EAX, AX
B:	OR	EAX, EAX
	JNS	PLUS
	NEG	EAX
	MOV	BYTE PTR [SI], '-'
	INC	SI
PLUS:	MOV	EBX, 10
	CALL	RADIX
	MOV	BYTE PTR [SI], '$'
	LEA	DX, BUF
	MOV	AH, 9
	INT	21H
	POP	SI
	POP	EBX
	RET
F2T10	ENDP
CODE	ENDS
	END
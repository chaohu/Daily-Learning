       	ASSUME	CS:CODE,DS:DATA,SS:STACK
START:  MOV 	AX,DATA
	MOV 	DS,AX
	LEA	BX,TIPS
	CALL	PLUS
	MOV	A,AX
	CALL	PLUS
	ADD	AX,A
